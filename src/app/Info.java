    private void totalFineCollected(Connection con) throws SQLException, ParseException {
        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery("SELECT created_at, returned_at FROM issue_logs WHERE returned_at IS NOT NULL");

        long fine = 0;
        do {
            String created_at = rs.getString("created_at");
            String returned_at = rs.getString("returned_at");
            fine += calculateFine(returned_at, created_at);
        } while (rs.next());

        String totalFine = String.valueOf(fine);
        this.jLabel10.setText("â‚¹ " + totalFine);
    }


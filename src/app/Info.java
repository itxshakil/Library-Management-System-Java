    private void loadInfo() {
        try {
            Connection con = new ConnectionService().getConnection();
            if (con == null) {
                JOptionPane.showMessageDialog(null, "Error while connecting database.");
            } else {
                totalBooks(con);
                totalUsers(con);
                totalIssued(con);
                totalAvailableBook(con);
                totalFineCollected(con);
                totalIssues(con);

            }
        } catch (HeadlessException | SQLException ex) {
            System.out.println("SQLException: " + ex);
        } catch (ParseException ex) {
            System.out.println("ParseException: " + ex);
        }
    }

